
package anisul_tuto;

public class WrapperClass {
    
    public static void main(String[] args) {
        
        //primitive -> object 
        
        int x=30;
        Integer y=x;
        
        System.out.println("y = "+y);
        
        
        //object -> primitive
        Double d=20.5;
        double b=d;
        System.out.println("b= "+b);
    }
    
}
